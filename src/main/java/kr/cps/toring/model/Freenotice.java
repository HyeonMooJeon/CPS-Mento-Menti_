package kr.cps.toring.model;

public class Freenotice {

	private int postnumber;
	private int menteenumber;
	private String headname;
	private String subject;
	private String name;
	private String timedate;
	private String filematerial;
	private String newfilematerial;
	
	public String getFilematerial() {
		return filematerial;
	}
	public void setFilematerial(String filematerial) {
		this.filematerial = filematerial;
	}
	public String getNewfilematerial() {
		return newfilematerial;
	}
	public void setNewfilematerial(String newfilematerial) {
		this.newfilematerial = newfilematerial;
	}
	
	
	
	
	public int getPostnumber() {
		return postnumber;
	}
	public void setPostnumber(int postnumber) {
		this.postnumber = postnumber;
	}
	public int getMenteenumber() {
		return menteenumber;
	}
	public void setMenteenumber(int menteenumber) {
		this.menteenumber = menteenumber;
	}
	public String getHeadname() {
		return headname;
	}
	public void setHeadname(String headname) {
		this.headname = headname;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTimedate() {
		return timedate;
	}
	public void setTimedate(String timedate) {
		this.timedate = timedate;
	}
	
	
}
